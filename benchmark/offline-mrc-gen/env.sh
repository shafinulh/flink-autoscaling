#!/usr/bin/env bash

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="${REPO_ROOT:-$(cd "${SCRIPT_DIR}/../.." && pwd)}"

QUERY_ID="${QUERY_ID:-q20u_no_filters}"
INPUT_DIR="${INPUT_DIR:-${SCRIPT_DIR}/inputs/${QUERY_ID}}"
OUTPUT_DIR="${OUTPUT_DIR:-${SCRIPT_DIR}/outputs/${QUERY_ID}}"
PLOTS_DIR="${PLOTS_DIR:-${SCRIPT_DIR}/plots/${QUERY_ID}}"
MRC_PLOTS_DIR="${MRC_PLOTS_DIR:-${PLOTS_DIR}/mrc}"
MRC_VS_GROUND_TRUTH_PLOTS_DIR="${MRC_VS_GROUND_TRUTH_PLOTS_DIR:-${PLOTS_DIR}/mrc_vs_ground_truth}"

RAW_TRACE_PATH="${RAW_TRACE_PATH:-${INPUT_DIR}/trace.trace}"
FULL_GROUND_TRUTH_PATH="${FULL_GROUND_TRUTH_PATH:-${INPUT_DIR}/ground_truth_mrc.txt}"
DATA_ONLY_GROUND_TRUTH_PATH="${DATA_ONLY_GROUND_TRUTH_PATH:-${INPUT_DIR}/ground_truth_mrc_data_only.txt}"
DATA_USER_ONLY_GROUND_TRUTH_PATH="${DATA_USER_ONLY_GROUND_TRUTH_PATH:-${INPUT_DIR}/ground_truth_mrc_data_user_only.txt}"

ONLINE_MRC_DIR="${ONLINE_MRC_DIR:-${REPO_ROOT}/online_mrc}"
ONLINE_MRC_BUILD_DIR="${ONLINE_MRC_BUILD_DIR:-${ONLINE_MRC_DIR}/build}"
BLOCK_CACHE_TRACE_ANALYZER="${BLOCK_CACHE_TRACE_ANALYZER:-}"
DEFAULT_SHARDS_SAMPLING="${DEFAULT_SHARDS_SAMPLING:-1e-2}"

if [[ -f "${SCRIPT_DIR}/env.local.sh" ]]; then
  # shellcheck disable=SC1091
  source "${SCRIPT_DIR}/env.local.sh"
fi

export SCRIPT_DIR
export REPO_ROOT
export QUERY_ID
export INPUT_DIR
export OUTPUT_DIR
export PLOTS_DIR
export MRC_PLOTS_DIR
export MRC_VS_GROUND_TRUTH_PLOTS_DIR
export RAW_TRACE_PATH
export FULL_GROUND_TRUTH_PATH
export DATA_ONLY_GROUND_TRUTH_PATH
export DATA_USER_ONLY_GROUND_TRUTH_PATH
export ONLINE_MRC_DIR
export ONLINE_MRC_BUILD_DIR
export BLOCK_CACHE_TRACE_ANALYZER
export DEFAULT_SHARDS_SAMPLING
